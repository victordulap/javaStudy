package com.step.calculator;

public class Calculator {
    public static int sum(int nr1, int nr2) {
        return nr1 + nr2;
    }

    public static int difference(int nr1, int nr2) {
        return nr1 - nr2;
    }

    public static int multiply(int nr1, int nr2) {
        return nr1 * nr2;
    }

    public static double division(int nr1, int nr2) {
        return (double) nr1 / nr2;
    }
}
